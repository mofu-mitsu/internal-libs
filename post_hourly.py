def get_time_period():
    hour = (datetime.utcnow().hour + 9) % 24  # JSTå¯¾å¿œ
    if 6 <= hour < 11:
        return "morning"
    elif 11 <= hour < 16:
        return "afternoon"
    elif 16 <= hour < 19:
        return "evening"
    elif 19 <= hour < 1 or hour == 0:
        return "night"
    else:
        return "midnight"

# ãƒ†ã‚­ã‚¹ãƒˆæ­£è¦åŒ–ï¼ˆå…¨è§’ãªã©ã®çµ±ä¸€ï¼‰
def normalize_text(text):
    return unicodedata.normalize("NFKC", text).strip()

# facetsç”Ÿæˆï¼ˆUTF-8ãƒã‚¤ãƒˆä½ç½®ã§ãƒãƒƒã‚·ãƒ¥ã‚¿ã‚°å¯¾å¿œï¼‰
def generate_facets_from_text(text, hashtags):
    text_bytes = text.encode("utf-8")
    facets = []

    for tag in hashtags:
        tag_bytes = tag.encode("utf-8")
        byte_start = text_bytes.find(tag_bytes)

        if byte_start != -1:
            facets.append({
                "index": {
                    "byteStart": byte_start,
                    "byteEnd": byte_start + len(tag_bytes)
                },
                "features": [{
                    "$type": "app.bsky.richtext.facet#tag",
                    "tag": tag.lstrip("#")
                }]
            })

    return facets

